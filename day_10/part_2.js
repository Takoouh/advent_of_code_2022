const formatData = (rawData) => {
  return rawData.split("\r\n").map((line) => line.split(" "));
};

const resolvePuzzle = (data) => {
  console.time("exec time");

  const formattedData = formatData(data);

  const cyclesToCheck = [40, 80, 120, 160, 200, 240];

  const cycleLoop = [1];
  formattedData.forEach((line) => {
    cycleLoop.push(cycleLoop[cycleLoop.length - 1] || 1);
    if (line[0] === "addx") cycleLoop.push(cycleLoop[cycleLoop.length - 1] + Number(line[1]));
  });

  const xValuesByLines = [...cyclesToCheck.map((cycle) => cycleLoop.slice(cycle - 40 > 0 ? cycle - 40 : 0, cycle))];

  xValuesByLines.forEach((line) => {
    let lineToPrint = "";
    line.forEach((xValue, index) => ([xValue - 1, xValue, xValue + 1].includes(index) ? (lineToPrint += "#") : (lineToPrint += ".")));
    console.log(lineToPrint);
  });
  console.timeEnd("exec time");
};

